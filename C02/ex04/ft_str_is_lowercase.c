#include<unistd.h>
int	ft_str_is_lowercase(char *str)
{
	int	r;
	int	i;

	r = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z'))
		{
			r = 1;
		}
		else
		{
			return (0);
		}
		i++;
	}
	return (r);
}
